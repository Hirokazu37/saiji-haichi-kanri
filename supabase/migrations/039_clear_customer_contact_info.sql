-- ============================================================
-- 顧客の住所・電話・郵便番号を削除（個人情報最小化）
-- ============================================================
-- 2026-06 方針決定: アプリで保持するのは 顧客番号・氏名・カナ のみ。
-- 来場登録（番号入力・名前検索）に住所や電話は不要であり、
-- 個人情報のリスクを下げるため、既に取り込んだ分も消去する。
-- 完全な顧客情報は産直くん11側にあるため、失われる情報はない。
-- ============================================================

UPDATE customers
SET postal_code = NULL,
    address     = NULL,
    phone       = NULL
WHERE postal_code IS NOT NULL
   OR address IS NOT NULL
   OR phone IS NOT NULL;
