-- ============================================================
-- 催事ごとのマネキン手配進捗ステータス
-- ============================================================
-- DMハガキ (dm_status) や 備品の流れ (shipment_status) と同じく、
-- 催事一覧から手配の進捗を一望できるようにする。
--
-- ステータス値:
--   NULL        = まだトラッキング対象に登録されていない
--   '未手配'    = 手配が必要だが未着手
--   '確定'      = マネキンとの手配が確定済み
--   '完了'      = 催事終了後・確認まで完了
-- ============================================================

ALTER TABLE events
  ADD COLUMN IF NOT EXISTS mannequin_arrangement_status TEXT;

COMMENT ON COLUMN events.mannequin_arrangement_status IS
  'マネキン手配ステータス (NULL / 未手配 / 確定 / 完了)。マネキンの手配一覧画面で進捗管理に使用。';
