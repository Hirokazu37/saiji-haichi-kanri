-- ============================================================
-- 産直君 汎用マスター区分 (DM顧客区分) テーブル + 初期データ
-- ============================================================
-- ヤマトシステム「産直君」の汎用マスター区分 (区分3〜10 × コード) を
-- 百貨店マスターと紐付けて管理する。
-- 元データ: 催事の区分整理_.xlsx (2026-06 時点の区分表)
--
-- - 1百貨店に複数区分 (上・地下・お試し等) を紐付け可能
-- - venue_id が NULL の行は百貨店マスター未登録 (DM区分マスター画面で後から紐付け)
-- ============================================================

CREATE TABLE IF NOT EXISTS sanchoku_segments (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  kbn_no SMALLINT NOT NULL,            -- 汎用マスター区分番号 (3〜10)
  code SMALLINT NOT NULL,              -- 区分内コード (101 など)
  segment_name TEXT NOT NULL,          -- 産直君上の名称
  venue_id UUID REFERENCES venue_master(id) ON DELETE SET NULL,
  notes TEXT,
  is_active BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT now(),
  updated_at TIMESTAMPTZ DEFAULT now(),
  UNIQUE (kbn_no, code)
);

CREATE INDEX IF NOT EXISTS idx_sanchoku_segments_venue ON sanchoku_segments(venue_id);

ALTER TABLE sanchoku_segments ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "認証ユーザーはDM区分を閲覧・編集可能" ON sanchoku_segments;
CREATE POLICY "認証ユーザーはDM区分を閲覧・編集可能" ON sanchoku_segments
  FOR ALL USING (auth.role() = 'authenticated');

